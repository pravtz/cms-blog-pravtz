interface Props {
  pendingCount: number
}

export default function DynamicSidebar({ pendingCount }: Props) {
  const pathname = usePathname()
  const [mobileOpen, setMobileOpen] = useState(false)

  // Close mobile drawer on navigation
  useEffect(() => {
    setMobileOpen(false)
  }, [pathname])

  // Close on Escape
  useEffect(() => {
    function handleKey(e: KeyboardEvent) {
      if (e.key === 'Escape') setMobileOpen(false)
    }
    document.addEventListener('keydown', handleKey)
    return () => document.removeEventListener('keydown', handleKey)
  }, [])

  const navContent = (
    <nav className={styles.nav} aria-label="Main navigation">
      <div className={styles.logo}>
        <span className={styles.logoIcon}>N</span>
        <span className={styles.logoText}>Nexus CMS</span>
      </div>
      <ul className={styles.navList} role="list">
        {NAV_ITEMS.map((item) => {
          const isActive = pathname === item.href || pathname.startsWith(item.href + '/')
          return (
            <li key={item.href}>
              <Link
                href={item.href}
                className={`${styles.navItem} ${isActive ? styles.active : ''}`}
                aria-current={isActive ? 'page' : undefined}
              >
                <span className={styles.navIcon}>{item.icon}</span>
                <span className={styles.navLabel}>{item.label}</span>
                {item.href === '/admin/users' && pendingCount > 0 && (
                  <span className={styles.badge} aria-label={`${pendingCount} pending`}>
                    {pendingCount > 99 ? '99+' : pendingCount}
                  </span>
                )}
              </Link>
            </li>
          )
        })}
      </ul>
    </nav>
  )

  return (
    <>
      {/* Desktop sidebar */}
      <aside className={styles.sidebar} aria-label="Sidebar">
        {navContent}
      </aside>

      {/* Mobile hamburger button */}
      <button
        className={styles.mobileToggle}
        onClick={() => setMobileOpen(true)}
        aria-label="Open navigation"
        aria-expanded={mobileOpen}
      >
        <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round" aria-hidden="true">
          <line x1="3" y1="6" x2="21" y2="6" />
          <line x1="3" y1="12" x2="21" y2="12" />
          <line x1="3" y1="18" x2="21" y2="18" />
        </svg>
      </button>

      {/* Mobile overlay + drawer (only rendered when open so focus is trapped properly
          and the drawer isn't reachable via tab/screen reader when closed) */}
      {mobileOpen && (
        <>
          <div
            className={styles.overlay}
            onClick={() => setMobileOpen(false)}
            aria-hidden="true"
          />
          <aside
            className={`${styles.drawer} ${styles.drawerOpen}`}
            aria-label="Navigation drawer"
            role="dialog"
            aria-modal="true"
          >
            <button
              className={styles.drawerClose}
              onClick={() => setMobileOpen(false)}
              aria-label="Close navigation"
            >
              <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round" aria-hidden="true">
                <line x1="18" y1="6" x2="6" y2="18" />
                <line x1="6" y1="6" x2="18" y2="18" />
              </svg>
            </button>
            {navContent}
          </aside>
        </>
      )}
    </>
  )
}
